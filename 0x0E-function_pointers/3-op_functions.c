#include "3-calc.h"

/**
 * op_add - addition
 * @a: first argument
 * @b: second argument
 * Return: the result of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction
 * @a: first argument
 * @b: second argument
 * Return: the result of a substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first argument
 * @b: second argument
 * Return: the result of multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: first argument
 * @b: second argument
 * Return: the result of a division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus operator
 * @a: first argument
 * @b: second argument
 * Return: the result of modulus operator
 */
int op_mod(int a, int b)
{
	return (a % b);
}
